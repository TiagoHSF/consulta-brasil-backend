package com.consulta.brasil.model;

public class BancoDTO {
    /** The ispb */
    private String ispb;

    /** The name */
    private String name;

    /** The code */
    private Integer code;

    /** The full name */
    private String fullName;

    /**
     * Gets the ispb
     * @return
     */
    public String getIspb() {
        return ispb;
    }

    /**
     * Sets the ispb
     * @param ispb
     */
    public void setIspb(String ispb) {
        this.ispb = ispb;
    }

    /**
     * Gets the name
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets the code
     * @return
     */
    public Integer getCode() {
        return code;
    }

    /**
     * Sets the code
     * @param code
     */
    public void setCode(Integer code) {
        this.code = code;
    }

    /**
     * Gets the full name
     * @return
     */
    public String getFullName() {
        return fullName;
    }

    /**
     * Sets the full name
     * @param fullName
     */
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    
}
